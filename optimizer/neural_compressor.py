class DomainClassifier:
    """领域分类器"""
    
    def classify(self, text: str) -> CompressionContext:
        """分类文本领域"""
        context = CompressionContext()
        
        # 技术领域关键词
        tech_keywords = {'代码', '函数', 'API', '数据库', '服务器', '编程', 'Python', 'Java'}
        # 法律领域关键词
        legal_keywords = {'合同', '法律', '条款', '协议', '诉讼', '法院', '律师'}
        # 医疗领域关键词
        medical_keywords = {'疾病', '症状', '治疗', '药物', '医院', '医生', '诊断'}
        
        text_lower = text.lower()
        
        # 计算各领域得分
        tech_score = sum(1 for k in tech_keywords if k in text)
        legal_score = sum(1 for k in legal_keywords if k in text)
        medical_score = sum(1 for k in medical_keywords if k in text)
        
        # 确定领域
        scores = {'tech': tech_score, 'legal': legal_score, 'medical': medical_score}
        max_domain = max(scores, key=scores.get)
        
        if scores[max_domain] > 0:
            context.domain = max_domain
        
        return context


