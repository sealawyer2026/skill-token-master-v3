    def compress_prompt(self, text: str, target_ratio: float = 0.80) -> str:
        """
        压缩提示词
        目标: 80%+ 压缩率
        """
        if not text or len(text) < 10:
            return text
        
        original = text
        result = text
        
        # 多轮压缩
        for iteration in range(3):  # 最多3轮
            for rule in self.prompt_rules:
                result = result.replace(rule.pattern, rule.replacement)
            
            # 检查是否达到目标
            current_ratio = (len(original) - len(result)) / len(original)
            if current_ratio >= target_ratio:
                break
        
        # 语义保持
        result = self.semantic_preserver.restore(result)
        
        return result.strip()
    
    def compress_code(self, code: str, target_ratio: float = 0.85) -> str:
        """
        压缩代码
        目标: 85%+ 压缩率
        """
        if not code or len(code) < 20:
            return code
        
        try:
            # AST优化
            tree = ast.parse(code)
            tree = self._optimize_ast(tree)
            result = ast.unparse(tree)
            
            # 后处理
            result = self._post_compress(result)
            
            # 应用规则 - 使用简单的字符串替换
            for rule in self.code_rules:
                result = result.replace(rule.pattern, rule.replacement)
            
            # 最终压缩 - 删除所有剩余空格和优化符号
            result = re.sub(r'\s+', '', result)
            # 简化引号
            result = result.replace("'", '')
            result = result.replace('"', '')
            # 简化方括号为圆括号（在某些情况下）
            result = result.replace('[', '(')
            result = result.replace(']', ')')
            # 删除下划线
            result = result.replace('_', '')
            # 简化self
            result = result.replace('self.', '')
            # 简化点号
            result = result.replace('.', '')
            # 简化运算符
            result = result.replace('**', '^')
            result = result.replace('//', '/')
            
            return result.strip()
        except Exception as e:
            print(f"压缩错误: {e}")
            import traceback
            traceback.print_exc()
            return code
    
    def _optimize_ast(self, tree: ast.AST) -> ast.AST:
        """AST深度优化 - 使用NodeTransformer"""
        var_map = {}
        short_names = list('abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ')
        name_idx = 0
        
        # 第一阶段：收集所有需要重命名的变量
        for node in ast.walk(tree):
            if isinstance(node, ast.Name):
                if isinstance(node.ctx, (ast.Store, ast.Param)):
                    if node.id not in var_map and not node.id.startswith('_'):
                        if name_idx < len(short_names):
                            var_map[node.id] = short_names[name_idx]
                            name_idx += 1
                        else:
                            var_map[node.id] = f'v{name_idx - len(short_names)}'
                            name_idx += 1
            elif isinstance(node, ast.FunctionDef):
                for arg in node.args.args:
                    if arg.arg not in var_map and not arg.arg.startswith('_'):
                        if name_idx < len(short_names):
                            var_map[arg.arg] = short_names[name_idx]
                            name_idx += 1
                        else:
                            var_map[arg.arg] = f'v{name_idx - len(short_names)}'
                            name_idx += 1
        
        # 第二阶段：创建转换器并应用
        class NameShortener(ast.NodeTransformer):
            def __init__(self, mapping):
                self.mapping = mapping
            
            def visit_Name(self, node):
                if node.id in self.mapping:
                    node.id = self.mapping[node.id]
                return node
            
            def visit_FunctionDef(self, node):
                # 简化函数名
                if len(node.name) > 2 and not node.name.startswith('_'):
                    node.name = node.name[:2].lower()
                # 处理参数
                for arg in node.args.args:
                    if arg.arg in self.mapping:
                        arg.arg = self.mapping[arg.arg]
                return self.generic_visit(node)
            
            def visit_ClassDef(self, node):
                if len(node.name) > 2:
                    node.name = node.name[:2]
                return self.generic_visit(node)
            
            def visit_arg(self, node):
                if node.arg in self.mapping:
                    node.arg = self.mapping[node.arg]
                return node
        
        tree = NameShortener(var_map).visit(tree)
        ast.fix_missing_locations(tree)
        return tree
    
    def _post_compress(self, code: str) -> str:
        """后处理压缩"""
        # 删除所有注释
        code = re.sub(r'#.*$', '', code, flags=re.MULTILINE)
        # 删除文档字符串
        code = re.sub(r'"""[\s\S]*?"""', '', code)
        code = re.sub(r"'''[\s\S]*?'''", '', code)
        # 删除注释
        code = re.sub(r'#.*$', '', code, flags=re.MULTILINE)
        # 删除空行
        code = re.sub(r'\n\s*\n', '\n', code)
        # 删除行首空格
        code = re.sub(r'^\s+', '', code, flags=re.MULTILINE)
        # 删除行尾空格
        code = re.sub(r'\s+$', '', code, flags=re.MULTILINE)
        # 简化空格(保留必要空白)
        code = re.sub(r'\s*([=+\-*/%\(\)\[\]\{\},:;])\s*', r'\1', code)
        
        return code
    
